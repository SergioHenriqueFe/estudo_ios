import Foundation
// MARK: Estrutura De Repetição

//MARK:  While

var index:Int = 0

//enquanto index for <= 10 ele executa

while index <= 10 {
//    print("index: \(index)")
    index  = index + 1
}
//print("valor do indice final é : \(index)")


// MARK: Repeat

var indexrepeat: Int = 1

repeat {
//    print("index: \(indexrepeat)")
    indexrepeat += 1
} while indexrepeat <= 10


// MARK: For
// depois do in tem que ser uma sequência de números , array

for value in  10...50{
    print(value)
}












